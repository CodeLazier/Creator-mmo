	local t = {}
	local multi = {}
	for k,v in pairs(const) do
		if type(v) == "number" then
			if not t[v] then
				t[v] = k
			else
				if not multi[v] then multi[v] = {t[v]} end
				table.insert(multi[v],k)
			end
		end
	end
	for k,v in pairs(multi) do
		print(k,table.concat(v,", "))
	end
end
